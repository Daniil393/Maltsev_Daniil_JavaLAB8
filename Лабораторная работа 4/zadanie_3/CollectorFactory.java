package zadanie_3;

/**
 * Интерфейс фабрики для создания новых экземпляров коллекций или контейнеров.
 *
 * @param <P> тип создаваемой коллекции или контейнера
 */
public interface CollectorFactory<P> {

    /**
     * Создаёт и возвращает новый экземпляр коллекции или контейнера.
     *
     * @return новый объект типа {@code P}
     */
    P create();
}