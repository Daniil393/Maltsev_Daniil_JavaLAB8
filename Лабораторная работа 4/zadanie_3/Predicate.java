package zadanie_3;

/**
 * Интерфейс, представляющий условие (предикат) для объектов типа {@code T}.
 * <p>
 * Метод {@code test} проверяет, удовлетворяет ли объект заданному условию.
 *
 * @param <T> тип проверяемого объекта
 */
public interface Predicate<T> {

    /**
     * Проверяет, удовлетворяет ли объект {@code value} условию предиката.
     *
     * @param value объект для проверки
     * @return {@code true}, если объект удовлетворяет условию, иначе {@code false}
     */
    boolean test(T value);
}