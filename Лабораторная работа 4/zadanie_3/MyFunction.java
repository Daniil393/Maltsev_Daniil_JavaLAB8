package zadanie_3;

/**
 * Функциональный интерфейс, представляющий функцию, которая принимает объект типа {@code T}
 * и возвращает объект типа {@code P}.
 *
 * @param <T> тип входного аргумента функции
 * @param <P> тип возвращаемого значения функции
 */
@FunctionalInterface
public interface MyFunction<T, P> {

    /**
     * Применяет функцию к объекту {@code t}.
     *
     * @param t входной аргумент
     * @return результат функции
     */
    P apply(T t);
}